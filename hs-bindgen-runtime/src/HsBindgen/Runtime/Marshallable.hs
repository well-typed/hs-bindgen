{- $foreign-types

The 'Marshallable' class broadly captures Haskell types that are marshallable
through the FFI. By marshallable we mean: types that are valid in a foreign
import. The "Haskell 2010 Language" report provides grammars for types that are
considered marshallable. These grammars include:

* /basic foreign types/ (technically not specified as a proper pgrammar)
* /foreign types/
* /marshallable foreign result types/
* /marshallable foreign types/

See the "8.4.2 Foreign Types" section of the report for more information:
<https://www.haskell.org/onlinereport/haskell2010/haskellch8.html#x15-1560008.4.2>

This module comes with 'Marshallable' instances for the rules in these grammars,
plus additional instances for types from @base@ that are not described in the
report as being marshallable, such as types from the "Foreign.C.Types" module.

=== Soundness and completeness

In the report, the grammars for marshallable types are separate. In our
'Marshallable' class, we conflate these grammars for the sake of simplicity,
because it is complex to encode the grammars into separate classes precisely.
This has the caveat that the class and the default instances provided in this
module are not /sound/ with respect to marshallable types as described in the
Haskell2010 report. For example:

> foreign import ... foo :: IO CInt -> IO CInt

The type of @foo@ is 'Marshallable', but it is not a valid type for a foreign
import. 'IO' should (for the most part) only appear in a function result.

Moreover, instances of the 'Marshallable' class are not generated magically for
newtypes. This means that the class and its default instances provided in this
module are not /complete/ with respect to marshallable types as described in the
Haskell2010 report. For example:

> newtype MyCInt = MyCInt CInt
> foreign import ... bar :: IO MyCInt -> IO CInt

The type of @bar@ is not 'Marshallable', but it /is/ a valid type for a foreign
import, because the compiler can see that 'MyCInt' coerces to a 'CInt'.

=== Instances generated by hs-bindgen

@hs-bindgen@ generates 'Marshallable' instances for the newtypes that it
generates if the underlying type has a 'Marshallable' instance as well. It does
this using newtype-deriving:

> newtype S = S CInt
>   deriving newtype Marshallable
> newtype T a = T a
>   deriving newtype Marshallable
> newtype U = U (CChar -> IO CBool)
>   deriving newtype Marshallable

If a user wants to provide an instance for a (custom) newtype, then they should
be able to define one using newtype-deriving, provided the underlying type has a
'Marshallable' instance of course.
-}

