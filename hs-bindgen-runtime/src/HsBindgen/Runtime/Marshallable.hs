-- | Haskell types that are marshallable through the FFI.
--
-- As a rule of thumb, if a type can appear in a foreign import, then it
-- /should/ have a 'Marshallable'.
class Marshallable a where
  type MarshallableBaseType a :: Type
  type MarshallableBaseType a = a
  toBaseType :: a -> MarshallableBaseType a
  fromBaseType :: MarshallableBaseType a -> a

toMarshallableBaseType :: Marshallable a => a -> MarshallableBaseType a
toMarshallableBaseType = toBaseType

fromMarshallableBaseType :: Marshallable a => MarshallableBaseType a -> a
fromMarshallableBaseType = fromBaseType

{-------------------------------------------------------------------------------
  Deriving-via
-------------------------------------------------------------------------------}

newtype ViaIdentity a = ViaIdentity a

instance Marshallable (ViaIdentity a) where
  type MarshallableBaseType (ViaIdentity a) = a

  {-# INLINE toBaseType #-}
  toBaseType (ViaIdentity x) = x

  {-# INLINE fromBaseType #-}
  fromBaseType x = ViaIdentity x

newtype ViaNewtype a = ViaNewtype a

instance Marshallable a => Marshallable (ViaNewtype a) where
  type MarshallableBaseType (ViaNewtype a) = MarshallableBaseType a

  {-# INLINE toBaseType #-}
  toBaseType (ViaNewtype x) = toBaseType x

  {-# INLINE fromBaseType #-}
  fromBaseType x = ViaNewtype (fromBaseType x)

{-------------------------------------------------------------------------------
  Instances: foreign types
-------------------------------------------------------------------------------}

instance (Marshallable a, Marshallable b) => Marshallable (a -> b) where
  type MarshallableBaseType (a -> b) = MarshallableBaseType a -> MarshallableBaseType b

  {-# INLINE toBaseType #-}
  toBaseType f = \x -> toBaseType (f (fromBaseType x))

  {-# INLINE fromBaseType #-}
  fromBaseType f = \x -> fromBaseType (f (toBaseType x))

{-------------------------------------------------------------------------------
  Instances: marshallable foreign result types
-------------------------------------------------------------------------------}

deriving via ViaIdentity () instance Marshallable ()

instance Marshallable a => Marshallable (IO a) where
  type MarshallableBaseType (IO a) = IO (MarshallableBaseType a)

  {-# INLINE toBaseType #-}
  toBaseType = fmap toBaseType

  {-# INLINE fromBaseType #-}
  fromBaseType = fmap fromBaseType

